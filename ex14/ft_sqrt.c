int		ft_sqrt(int nb)
{
	int	a;

	a = 1;
	if (nb < 1)
		return (0);
	else if (nb > 0 && nb <= 2147483647)
	{
		while ((a * a) <= nb)
		{
			if ((a * a) == nb)
				return (a);
			else
				a++;
		}
		if ((a * a) > nb)
			return (0);
	}
	else
		return (0);
	return (0);
}
