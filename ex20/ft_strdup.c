#include <stdlib.h>

char		*ft_strdup(char *src)
{
	int		i;
	char	*a;

	a = (char*)malloc(*src);
	i = 0;
	while (src[i] != '\0')
	{
		a[i] = src[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
